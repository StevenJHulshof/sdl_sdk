#include "resource_raw_create.h"

void createRawStone(int xPos, int yPos)
{
	RawStone *rawStone = new RawStone(xPos, yPos);
	gGlobalObjectPool._rawStonePool.push_back(rawStone);
}

void createRawWood(int xPos, int yPos)
{
	RawWood *rawWood = new RawWood(xPos, yPos);
	gGlobalObjectPool._rawWoodPool.push_back(rawWood);
}
